//VAR e LET - Declarar Variavel

//Declarando VAR
var texto = "Hello World"
console.log(texto)

//Declarando LET
let nome = "João Pedro"
console.log("Nome:",nome)

//Atribuindo um novo valor - LET
nome = "Guilherme"
console.log("Nome:",nome)

//Atribuindo um novo valor - VAR
texto = "Olá Mundo"
console.log(texto)

//Constantes - const
const ValorPi = 3.14
console.log("O valor de pi é igual a:",ValorPi)