//Operadores Aritmeticos
let n1 = 5
let n2 = 2

//Operador Soma
let soma = n1 + n2
console.log(soma)

//Operador Subtração
let subt = n1 - n2
console.log(subt)

//Operador Multiplicação
let mult = n1 * n2
console.log(mult)

//Operador Divisão
let div = n1 / n2
console.log(div)

//Operador Módulo (Pegar a Sobra da Divisão)
let resto = n1 % n2
console.log(resto)

//Operador de Potência
let potencia = Math.pow(n1,n2)
console.log(potencia)

//Operador Raiz Quadrada
let raizquad = Math.sqrt(n1)
console.log(raizquad)
